    <h2>Stub</h2>
    <p>
        When we create any model or controller etc the code that is present inside the file like any function or any page linking, that all are written in the stub files. <br>
        So to add some additional code to the file that you frequently needed after creating the file, thats why <b>stub</b> is used for.
        <br><br>
        To access the file first we have to publish the stub folder by running the code in the terminal -- <br>
        <b>php artisan stub:publish</b>
        <br>
        After publishing the folder add the codes to the required files.
    </p>
